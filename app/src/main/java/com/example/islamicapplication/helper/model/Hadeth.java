package com.example.islamicapplication.helper.model;

public class Hadeth {

    private String mTitle;
    private String mContent;

    public Hadeth(String mTitle, String mContent) {
        this.mTitle = mTitle;
        this.mContent = mContent;
    }

    public String getmTitle() {
        return mTitle;
    }

    public String getmContent() {
        return mContent;
    }
}
